#include <unistd.h>

void	ft_writeint(int *i)
{
	char	r;

	r = *i / 10 + '0';
	write (1, &r, 1);
	r = *i % 10 + '0';
	write (1, &r, 1);
}

void	ft_print_comb2(void)
{
	int		i[2];

	i[0] = 0;
	i[1] = 1;
	while (i[0] <= 98)
	{
		ft_writeint(&i[0]);
		write (1, " ", 1);
		ft_writeint(&i[1]);
		i[1]++;
		if (i[1] > 99)
		{
			i[0]++;
			i[1] = i[0] + 1;
		}
		if (i[0] <= 98)
			write (1, ", ", 2);
	}
}
