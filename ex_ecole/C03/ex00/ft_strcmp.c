int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	r;

	r = 0;
	i = 0;
	while ((s1[i] != '\0') && (s2[i] != '\0') && (r == 0))
	{
		if (s1[i] > s2[i] || s1[i] < s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}
